<h2>Programmi "Lahe lugu" infofail</h2>

<b>Millega tegu & kuidas kasutada</b><br>
Programm genereerib teie poolt sisestatud sõnadest random nalja jutu.<br>
Programmi sisenemisel kuvatakse ekraanile ankeet, kus on 
lahti seletatult kirjas, mis lahtrid tuleb täita ja mis on selle eesmärk. 
Täites ankeedi ja vajutades "vaata enda lugu" nuppu, genereerib programm 
jutukese teie poolt sisestatud andmetega.<br>
Uue jutu valimiseks tuleks vajutada refreshi ning seejärel ilmub uus 
ankeet uute andmetega ning sisestades lahtritesse uued andmed, 
koostatakse uus jutuke.<br>
Programm peab võimaldama vaadata ka originaaltekste, et siis neid soovi 
korral muudetud tekstidega oleks võimalik võrrelda.<br>
